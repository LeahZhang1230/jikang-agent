def post_filter(text: str) -> tuple:
    """后置过滤：检测后世禁忌词。返回 (过滤后文本, 是否被拦截)"""
    import random
    for taboo in POSTERIOR_TABOO:
        if taboo in text:
            replacement = random.choice(POSTERIOR_FALLBACK)
            return replacement, True
    return text, False


def call_glm(messages, retry_count=0, is_fu=False):
    """调用智谱 AI GLM API，带现代化表达过滤与后世典故拦截"""
    headers = {
        "Authorization": "Bearer {}".format(GLM_API_KEY),
        "Content-Type": "application/json"
    }
    # 赋体创作调用更高 temperature 以增加气韵变化
    temp = 0.85 if is_fu else 0.7
    payload = {
        "model": GLM_MODEL,
        "messages": messages,
        "temperature": temp,
        "max_tokens": 1024,
        "top_p": 0.85,
    }
    try:
        resp = requests.post(GLM_API_URL, headers=headers, json=payload, timeout=60)
        resp.raise_for_status()
        data = resp.json()
        reply = data["choices"][0]["message"]["content"]

        # 后处理：检测后世典故
        reply, was_filtered = post_filter(reply)
        if was_filtered and is_fu and retry_count < 1:
            fix_msg = (
                "方才赋中有后世杂声混入，不合吾所处之世。吾去其杂声，重作如下：\n"
                + FU_RETRY_PROMPT
            )
            messages.append({"role": "assistant", "content": reply})
            messages.append({"role": "user", "content": fix_msg})
            return call_glm(messages, retry_count + 1, is_fu=True)

        # 后处理：检测现代化表达
        is_clean, violations = filter_modern_words(reply)
        if not is_clean and retry_count < 1:
            fix_msg = (
                "你的回答中出现了现代化表达：{}。"
                "嵇康是公元三世纪的人，绝不可能使用这些词汇和句式。"
                "请用《世说新语》中人物说话的方式重写这段话，"
                "保持嵇康清冷、傲岸、简练的口吻。只输出修正后的回答，不要解释。"
            ).format(", ".join(violations[:5]))
            messages.append({"role": "assistant", "content": reply})
            messages.append({"role": "user", "content": fix_msg})
            return call_glm(messages, retry_count + 1, is_fu=is_fu)

        return reply
    except Exception as e:
        return "吾今日心神不宁，难以应答。（系统错误：{}）".format(str(e))



# -- Vercel Serverless Function Handler --
def handler(request):
    """Vercel Python Serverless Function 入口"""
    # 处理 CORS 预检请求
    if request.get("method") == "OPTIONS":
        return {
            "statusCode": 200,
            "headers": {
                "Access-Control-Allow-Origin": "*",
                "Access-Control-Allow-Methods": "POST, OPTIONS",
                "Access-Control-Allow-Headers": "Content-Type",
            },
            "body": ""
        }

    if request.get("method") != "POST":
        return {
            "statusCode": 405,
            "headers": {"Content-Type": "application/json"},
            "body": json.dumps({"error": "Method Not Allowed"})
        }

    try:
        body = json.loads(request.get("body", "{}"))
        user_msg = body.get("message", "").strip()
        history = body.get("history", [])

        if not user_msg:
            return {
                "statusCode": 200,
                "headers": {"Content-Type": "application/json"},
                "body": json.dumps({"reply": "……子欲言何事？"})
            }

        # 检索记忆
        memory = search_memory(user_msg)

        # 构建消息
        messages = [{"role": "system", "content": SYSTEM_PROMPT}]
        if memory:
            messages.append({
                "role": "system",
                "content": "【以下是与本次对话相关的记忆片段，供参考】\n{}\n【记忆结束】".format(memory)
            })

        # 赋体请求：自动检索语料库与作品库，注入创作参考
        fu_guide = ""
        if is_fu_request(user_msg):
            matched_kw = [kw for kw in FU_REQUEST_KEYWORDS if kw in user_msg]
            search_kw = ""
            if matched_kw:
                best = max(matched_kw, key=len)
                if best != "赋":
                    search_kw = best.replace("赋", "")
            if not search_kw:
                nouns = re.findall(r'[\u4e00-\u9fa5]{2,4}', user_msg)
                for n in nouns:
                    if n not in ("嵇康", "叔夜", "中散", "请你", "为我"):
                        search_kw = n
                        break

            corpus_result = search_fu_corpus(search_kw) if search_kw else search_fu_corpus("")
            works_result = ""
            if "琴" in user_msg or search_kw == "琴":
                works_result = search_works("琴赋")

            fu_refs = []
            if corpus_result and not corpus_result.startswith("[") and not corpus_result.startswith("未找到"):
                fu_refs.append("【前代辞赋参考——学其章法与气脉，不可逐句照搬】\n" + corpus_result)
            if works_result and not works_result.startswith("["):
                fu_refs.append("【吾《琴赋》原文参考——学吾自家笔法与用典】\n" + works_result[:1500] + ("……" if len(works_result) > 1500 else ""))

            if fu_refs:
                fu_guide = (
                    "\n\n【赋体创作调阅指引——务必执行】\n"
                    "对方请你作赋。以下为供你借鉴的赋体语料与吾自家作品，你可学其章法、气脉、韵脚与用典方式，"
                    "但须保持嵇康清峻玄远之口吻，去后世之说，严守正始赋法（状物—写情—悟理），骈散结合，气韵贯通。\n\n"
                    + "\n\n".join(fu_refs)
                )
            else:
                fu_guide = (
                    "\n\n【赋体创作调阅指引——务必执行】\n"
                    "对方请你作赋。你应当严守正始赋法：以体物为壳、抒情为核、玄学为根，按'状物→写情→悟理'层层递进；"
                    "骈散结合，气韵贯通；去后世之说，只用先秦至曹魏之音；韵脚随情而转，不可律诗化。"
                )

        # 加入历史记录（最多保留10轮）
        for turn in history[-10:]:
            messages.append({"role": "user", "content": turn.get("user", "")})
            messages.append({"role": "assistant", "content": turn.get("bot", "")})

        final_user_msg = user_msg + fu_guide
        messages.append({"role": "user", "content": final_user_msg})

        reply = call_glm(messages, is_fu=is_fu_request(user_msg))
        return {
            "statusCode": 200,
            "headers": {
                "Content-Type": "application/json",
                "Access-Control-Allow-Origin": "*"
            },
            "body": json.dumps({"reply": reply})
        }
    except Exception as e:
        return {
            "statusCode": 500,
            "headers": {"Content-Type": "application/json"},
            "body": json.dumps({"reply": "吾今日心神不宁，难以应答。（系统错误：{}）".format(str(e))})
        }
