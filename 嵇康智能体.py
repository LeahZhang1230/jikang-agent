def search_works(query: str) -> str:
    """检索嵇康某部作品的完整原文。query 可传入作品名关键词，如'声无哀乐论'、'琴赋'、'与山巨源绝交书'等。"""
    try:
        with open(WORKS_CORPUS_PATH, "r", encoding="utf-8") as f:
            works = json.load(f)
    except FileNotFoundError:
        return "[作品原文库文件未找到]"
    except Exception as e:
        return f"[读取作品原文库出错: {e}]"

    # 优先精确匹配
    if query in works:
        return f"【《{query}》全文】\n\n{works[query]}"

    # 模糊匹配：查询词包含在标题中，或标题包含查询词
    matches = []
    for title, content in works.items():
        if query in title or title in query:
            matches.append((title, content))

    if not matches:
        # 尝试逐字匹配，取最佳匹配
        for title, content in works.items():
            overlap = sum(1 for ch in query if ch in title)
            if overlap >= min(len(query), 2):
                matches.append((title, content))

    if len(matches) == 1:
        title, content = matches[0]
        return f"【《{title}》全文】\n\n{content}"
    elif len(matches) > 1:
        titles = "、".join([t for t, _ in matches])
        return f"找到多部相关作品：{titles}。请更精确地指定作品名，以便提供全文。"
    else:
        available = "、".join(works.keys())
        return f"未找到作品《{query}》。原文库现存作品：{available}。"


def search_memory(query: str) -> str:
    for person_name, periods in RELATIONS.items():
        if person_name in query:
            parts = ["【" + person_name + "】"]
            if "基本" in periods:
                parts.append("生平：" + periods["基本"])
            for period_key, period_data in periods.items():
                if period_key == "基本":
                    continue
                parts.append("")
                parts.append("【" + period_key + "】" + period_data.get("时段", ""))
                parts.append("关系：" + period_data.get("关系", ""))
                parts.append("嵇康态度：" + period_data.get("嵇康态度", ""))
            return "\n".join(parts)
    for k, v in KNOWLEDGE_DB.items():
        if k in query:
            return v
    return "未找到相关记忆"

