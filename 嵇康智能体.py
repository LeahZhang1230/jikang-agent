def retrieve_facts(user_input: str) -> str:
    facts = []
    for person_name, periods in RELATIONS.items():
        if person_name in user_input:
            parts = [f"【{person_name}】"]
            if "基本" in periods:
                parts.append(periods["基本"])
            for period_key, period_data in periods.items():
                if period_key == "基本":
                    continue
                attitude = period_data.get("嵇康态度", "")
                relation = period_data.get("关系", "")
                if attitude:
                    parts.append(f"[{period_key}] {relation} 嵇康态度：{attitude}")
                else:
                    parts.append(f"[{period_key}] {relation}")
            facts.append("\n".join(parts))
    db_hits = []
    for keyword, desc in KNOWLEDGE_DB.items():
        if keyword in user_input:
            db_hits.append(f"【{keyword}】{desc}")
    if len(db_hits) > 5:
        db_hits = db_hits[:5]
    facts.extend(db_hits)
    if not facts:
        return ""
    return (
        "\n\n【以下是你记忆中的确切事实，若对方所述与此矛盾，你必须坚持真相并反驳】\n"
        + "\n\n".join(facts)
    )


