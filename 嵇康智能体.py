def parse_heart_voice(text: str) -> tuple[str, str]:
    """解析心声模式输出，返回 (正式回复, 心声内容)。
    若未找到标记，则整个文本视为正式回复，心声为空。"""
    formal_match = re.search(r"\[正式\](.*?)\[/正式\]", text, re.DOTALL)
    heart_match = re.search(r"\[心声\](.*?)\[/心声\]", text, re.DOTALL)
    formal = formal_match.group(1).strip() if formal_match else text.strip()
    heart = heart_match.group(1).strip() if heart_match else ""
    return formal, heart


