def search_fu_corpus(query: str = "") -> str:
    """检索《文选》嵇康以前辞赋语料库。query可传入主题关键词（如'琴''山水''风''云''游猎''神女''归隐''抒情''玄思''物色'等），返回最相关的赋段；query为空则返回目录导引。"""
    try:
        with open(FU_CORPUS_PATH, "r", encoding="utf-8") as f:
            content = f.read()
    except FileNotFoundError:
        return "[语料库文件未找到]"
    except Exception as e:
        return f"[读取语料库出错: {e}]"

    # 按 <WORK> 块分割并解析
    blocks = re.split(r'\n<WORK>\n', content)
    parsed = []
    for block in blocks:
        block = block.strip()
        if not block or block.startswith("《文选》") or block.startswith("版本：") or block.startswith("用途：") or block.startswith("说明：") or block.startswith("【训练总指令】") or block.startswith("="):
            continue

        title_match = re.search(r'篇名[：:](.*?)(?:\n|$)', block)
        author_match = re.search(r'作者[：:](.*?)(?:\n|$)', block)
        tags_match = re.search(r'标签[：:](.*?)(?:\n|$)', block)
        era_match = re.search(r'时代[：:](.*?)(?:\n|$)', block)

        title = title_match.group(1).strip() if title_match else "未知"
        author = author_match.group(1).strip() if author_match else "未知"
        tags = tags_match.group(1).strip() if tags_match else ""
        era = era_match.group(1).strip() if era_match else ""

        body_start = block.find("正文")
        if body_start != -1:
            body = block[body_start+2:].strip()
            if body.startswith("：") or body.startswith(":"):
                body = body[1:].strip()
        else:
            body = block

        parsed.append({"title": title, "author": author, "era": era, "tags": tags, "body": body})

    if not query or query.strip() == "":
        lines = ["【赋体语料库目录——嵇康以前辞赋】", "可搜索主题关键词：琴、山水、风、云、游猎、神女、梦境、归隐、抒情、玄思、京殿、物色等\n"]
        for p in parsed[:40]:
            lines.append(f"· {p['title']}（{p['author']}，{p['era']}）标签：{p['tags']}")
        return "\n".join(lines)

    q = query.lower()
    scored = []
    for p in parsed:
        score = 0
        if q in p['title'].lower(): score += 10
        if q in p['tags'].lower(): score += 8
        if q in p['author'].lower(): score += 5
        if q in p['body'][:500].lower(): score += 3
        overlap = sum(1 for ch in query if ch in p['title']+p['tags']+p['body'][:300])
        score += overlap * 0.5
        if score > 0:
            scored.append((score, p))

    scored.sort(key=lambda x: x[0], reverse=True)
    if not scored:
        return f"未找到与「{query}」相关的赋段。请尝试搜索：琴、山水、风、云、游猎、神女、归隐、抒情、玄思、物色等关键词。"

    results = []
    for score, p in scored[:3]:
        excerpt = p['body'][:700] + ("……" if len(p['body']) > 700 else "")
        results.append(f"【{p['title']} · {p['author']} · {p['era']}】标签：{p['tags']}\n{excerpt}")
    return "\n\n——\n\n".join(results)


