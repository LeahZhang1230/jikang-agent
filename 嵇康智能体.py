def _trim_messages(messages: list[dict], max_rounds: int) -> list[dict]:
    if not messages:
        return messages
    system_msgs = [messages[0]]
    chat_msgs = messages[1:]
    # 以 user 消息作为一轮对话的起点，确保裁剪后每轮上下文完整
    user_indices = [i for i, m in enumerate(chat_msgs) if m.get("role") == "user"]
    if len(user_indices) > max_rounds:
        keep_from = user_indices[-max_rounds]
        chat_msgs = chat_msgs[keep_from:]
    return system_msgs + chat_msgs


